import struct
def rotl32(value, shift):
    shift %= 32
    return ((value << shift) | (value >> (32 - shift))) & 0xFFFFFFFF


def nonlinear_transform(word32):
    b0 = SM4_SBOX[(word32 >> 24) & 0xFF]
    b1 = SM4_SBOX[(word32 >> 16) & 0xFF]
    b2 = SM4_SBOX[(word32 >> 8) & 0xFF]
    b3 = SM4_SBOX[word32 & 0xFF]
    return (b0 << 24) | (b1 << 16) | (b2 << 8) | b3
def round_transform(word32):
    x = nonlinear_transform(word32)
    return (x ^ rotl32(x, 2) ^ rotl32(x, 10) ^ rotl32(x, 18) ^ rotl32(x, 24)) & 0xFFFFFFFF
def key_transform(word32):
    x = nonlinear_transform(word32)
    return (x ^ rotl32(x, 13) ^ rotl32(x, 23)) & 0xFFFFFFFF
def expand_round_keys(master_key_bytes):
    mk0, mk1, mk2, mk3 = struct.unpack(">4I", master_key_bytes)
    k = [mk0 ^ FK[0], mk1 ^ FK[1], mk2 ^ FK[2], mk3 ^ FK[3]]
    for i in range(32):
        k.append((k[i] ^ key_transform(k[i + 1] ^ k[i + 2] ^ k[i + 3] ^ CK[i])) & 0xFFFFFFFF)
    return k[4:36]
def crypt_one_block(block16, round_keys):
    x0, x1, x2, x3 = struct.unpack(">4I", block16)
    for i in range(32):
        x0, x1, x2, x3 = (
            x1,
            x2,
            x3,
            (x0 ^ round_transform(x1 ^ x2 ^ x3 ^ round_keys[i])) & 0xFFFFFFFF
        )
    return struct.pack(">4I", x3, x2, x1, x0)
def pkcs7_pad(data, block_size=16):
    pad_len = block_size - (len(data) % block_size)
    return data + bytes([pad_len] * pad_len)
def pkcs7_unpad(data, block_size=16):
    if not data or len(data) % block_size != 0:
        raise ValueError("密文长度非法")
    pad_len = data[-1]
    if pad_len < 1 or pad_len > block_size or data[-pad_len:] != bytes([pad_len] * pad_len):
        raise ValueError("PKCS7填充非法")
    return data[:-pad_len]
def sm4_encrypt_bytes_ecb(plain_bytes, key_bytes, pad=True):
    if pad:
        plain_bytes = pkcs7_pad(plain_bytes, 16)
    elif len(plain_bytes) % 16 != 0:
        raise ValueError("pad=False 时明文长度必须是16的倍数")
    round_keys = expand_round_keys(key_bytes)
    out = bytearray()
    for i in range(0, len(plain_bytes), 16):
        out.extend(crypt_one_block(plain_bytes[i:i + 16], round_keys))
    return bytes(out)
def sm4_decrypt_bytes_ecb(cipher_bytes, key_bytes, unpad=True):
    if len(cipher_bytes) % 16 != 0:
        raise ValueError("密文长度必须是16的倍数")
    round_keys = expand_round_keys(key_bytes)[::-1]
    out = bytearray()
    for i in range(0, len(cipher_bytes), 16):
        out.extend(crypt_one_block(cipher_bytes[i:i + 16], round_keys))
    return pkcs7_unpad(bytes(out), 16) if unpad else bytes(out)
def sm4_encrypt(plain_text, key_text):
    key_bytes = key_text.encode("utf-8").ljust(16, b"\x00")[:16]
    return sm4_encrypt_bytes_ecb(plain_text.encode("utf-8"), key_bytes, pad=True)
def sm4_decrypt(cipher_bytes, key_text):
    key_bytes = key_text.encode("utf-8").ljust(16, b"\x00")[:16]
    plain_bytes = sm4_decrypt_bytes_ecb(cipher_bytes, key_bytes, unpad=True)
    return plain_bytes.decode("utf-8", errors="ignore")
def hamming_distance_bits(a: bytes, b: bytes):
    if len(a) != len(b):
        raise ValueError("长度必须一致")
    return sum((x ^ y).bit_count() for x, y in zip(a, b))


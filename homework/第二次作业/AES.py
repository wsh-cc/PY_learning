# 固定实验密钥（16字节）
DEFAULT_KEY = bytes.fromhex("00112233445566778899AABBCCDDEEFF")
def _b2s(b16: bytes):
    # 列优先
    return [[b16[r + 4 * c] for c in range(4)] for r in range(4)]
def _s2b(s):
    return bytes(s[r][c] for c in range(4) for r in range(4))
def _rot(wd):
    return wd[1:] + wd[:1]
def _subw(wd):
    return [SBOX[x] for x in wd]
def _expk(key16: bytes):
    # 44个word，每个word=4字节
    w = [[0, 0, 0, 0] for _ in range(44)]
    for i in range(4):
        w[i] = [key16[4 * i + j] for j in range(4)]

    for i in range(4, 44):
        temp = w[i - 1].copy()
        if i % 4 == 0:
            temp = _subw(_rot(temp))
            temp[0] ^= RCON[i // 4]
        w[i] = [w[i - 4][j] ^ temp[j] for j in range(4)]
    return w
def _ark(s, w, rnd):
    base = rnd * 4
    for c in range(4):
        for r in range(4):
            s[r][c] ^= w[base + c][r]
def _subb(s):
    for r in range(4):
        for c in range(4):
            s[r][c] = SBOX[s[r][c]]
def _isubb(s):
    for r in range(4):
        for c in range(4):
            s[r][c] = INV_SBOX[s[r][c]]
def _shiftr(s):
    for r in range(1, 4): s[r] = s[r][r:] + s[r][:r]
def _ishiftr(s):
    for r in range(1, 4): s[r] = s[r][-r:] + s[r][:-r]
def _xt(x):
    return (((x << 1) ^ 0x1B) & 0xFF) if (x & 0x80) else ((x << 1) & 0xFF)
def _gm(a, b):
    out = 0
    for _ in range(8):
        if b & 1:
            out ^= a
        a = _xt(a)
        b >>= 1
    return out & 0xFF
def _mixc(s):
    for c in range(4):
        a0, a1, a2, a3 = [s[r][c] for r in range(4)]
        s[0][c] = (_gm(a0, 2) ^ _gm(a1, 3) ^ a2 ^ a3) & 0xFF
        s[1][c] = (a0 ^ _gm(a1, 2) ^ _gm(a2, 3) ^ a3) & 0xFF
        s[2][c] = (a0 ^ a1 ^ _gm(a2, 2) ^ _gm(a3, 3)) & 0xFF
        s[3][c] = (_gm(a0, 3) ^ a1 ^ a2 ^ _gm(a3, 2)) & 0xFF
def _imixc(s):
    for c in range(4):
        a0, a1, a2, a3 = [s[r][c] for r in range(4)]
        s[0][c] = (_gm(a0, 14) ^ _gm(a1, 11) ^ _gm(a2, 13) ^ _gm(a3, 9)) & 0xFF
        s[1][c] = (_gm(a0, 9)  ^ _gm(a1, 14) ^ _gm(a2, 11) ^ _gm(a3, 13)) & 0xFF
        s[2][c] = (_gm(a0, 13) ^ _gm(a1, 9)  ^ _gm(a2, 14) ^ _gm(a3, 11)) & 0xFF
        s[3][c] = (_gm(a0, 11) ^ _gm(a1, 13) ^ _gm(a2, 9)  ^ _gm(a3, 14)) & 0xFF
def enc_blk(p16: bytes, k16: bytes) -> bytes:
    if len(p16) != 16 or len(k16) != 16:
        raise ValueError("明文和密钥都必须是16字节")
    w = _expk(k16)
    s = _b2s(p16)

    _ark(s, w, 0)
    for rnd in range(1, 10):
        _subb(s)
        _shiftr(s)
        _mixc(s)
        _ark(s, w, rnd)

    _subb(s)
    _shiftr(s)
    _ark(s, w, 10)
    return _s2b(s)
def dec_blk(c16: bytes, k16: bytes) -> bytes:
    if len(c16) != 16 or len(k16) != 16:
        raise ValueError("密文和密钥都必须是16字节")
    w = _expk(k16)
    s = _b2s(c16)

    _ark(s, w, 10)
    for rnd in range(9, 0, -1):
        _ishiftr(s)
        _isubb(s)
        _ark(s, w, rnd)
        _imixc(s)

    _ishiftr(s)
    _isubb(s)
    _ark(s, w, 0)
    return _s2b(s)
def _pad(data: bytes, bs=16) -> bytes:
    n = bs - (len(data) % bs)
    return data + bytes([n] * n)
def _unpad(data: bytes, bs=16) -> bytes:
    if not data or len(data) % bs != 0:
        raise ValueError("密文长度非法")
    n = data[-1]
    if n < 1 or n > bs or data[-n:] != bytes([n] * n):
        raise ValueError("填充错误")
    return data[:-n]
def enc_text(pt: str) -> str:
    """输入明文字符串 -> 输出密文hex"""
    data = _pad(pt.encode("utf-8"), 16)
    out = bytearray()
    for i in range(0, len(data), 16):
        out.extend(enc_blk(data[i:i+16], DEFAULT_KEY))
    return bytes(out).hex().upper()
def dec_hex(ch: str) -> str:
    """输入密文hex -> 输出明文字符串"""
    cipher = bytes.fromhex(ch.strip())
    if len(cipher) % 16 != 0:
        raise ValueError("密文hex对应字节数必须是16的倍数")
    out = bytearray()
    for i in range(0, len(cipher), 16):
        out.extend(dec_blk(cipher[i:i+16], DEFAULT_KEY))
    plain = _unpad(bytes(out), 16)
    return plain.decode("utf-8", errors="ignore")

